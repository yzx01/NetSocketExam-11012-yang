public class StartAndEnd {
    private int start;
    private int end;

    public StartAndEnd(int start, int end) {
        this.start = start;
        this.end = end;
    }

    public StartAndEnd() {
    }

    public int getStart() {
        return start;
    }

    public void setStart(int start) {
        this.start = start;
    }

    public int getEnd() {
        return end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
